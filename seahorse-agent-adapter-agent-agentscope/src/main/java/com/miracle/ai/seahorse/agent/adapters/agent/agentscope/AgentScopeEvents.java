package com.miracle.ai.seahorse.agent.adapters.agent.agentscope;

import io.agentscope.core.agent.Event;
import io.agentscope.core.agent.EventType;
import io.agentscope.core.message.Msg;
import io.agentscope.core.message.MsgRole;

@SuppressWarnings("deprecation")
final class AgentScopeEvents {

    private AgentScopeEvents() {
    }

    static Event finalAnswer(Msg message, boolean last) {
        Msg safeMessage = message == null
                ? Msg.builder().role(MsgRole.ASSISTANT).textContent("").build()
                : message;
        return new Event(EventType.AGENT_RESULT, safeMessage, last);
    }
}
