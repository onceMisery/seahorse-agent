package com.miracle.ai.seahorse.agent.kernel.application.consistency;

/**
 * 补偿日志（compensation log）条目。
 *
 * <p>记录需要重试的失败操作，包含：操作上下文、重试次数和下次重试时间。
 */
public class CompensationLog {

    private Long id;
    private String tenantId;
    private String operationType;
    private String operationId;
    private String payload;
    private CompensationStatus status;
    private int retryCount;
    private int maxRetries;
    private String lastError;

    public enum CompensationStatus {
        PENDING, RETRYING, SUCCESS, FAILED
    }

    // Getters and setters

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTenantId() {
        return tenantId;
    }

    public void setTenantId(String tenantId) {
        this.tenantId = tenantId;
    }

    public String getOperationType() {
        return operationType;
    }

    public void setOperationType(String operationType) {
        this.operationType = operationType;
    }

    public String getOperationId() {
        return operationId;
    }

    public void setOperationId(String operationId) {
        this.operationId = operationId;
    }

    public String getPayload() {
        return payload;
    }

    public void setPayload(String payload) {
        this.payload = payload;
    }

    public CompensationStatus getStatus() {
        return status;
    }

    public void setStatus(CompensationStatus status) {
        this.status = status;
    }

    public int getRetryCount() {
        return retryCount;
    }

    public void setRetryCount(int retryCount) {
        this.retryCount = retryCount;
    }

    public int getMaxRetries() {
        return maxRetries;
    }

    public void setMaxRetries(int maxRetries) {
        this.maxRetries = maxRetries;
    }

    public String getLastError() {
        return lastError;
    }

    public void setLastError(String lastError) {
        this.lastError = lastError;
    }
}
