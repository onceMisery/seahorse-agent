package com.miracle.ai.seahorse.agent.ports.outbound.ingestion;

import java.time.Instant;
import java.util.List;
import java.util.Objects;

/**
 * 入库 Pipeline 管理记录。
 */
public class IngestionPipelineRecord {

    private String id;
    private String name;
    private String description;
    private String createdBy;
    private Instant createTime;
    private Instant updateTime;
    private List<IngestionPipelineNodePayload> nodes = List.of();

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public Instant getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Instant createTime) {
        this.createTime = createTime;
    }

    public Instant getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Instant updateTime) {
        this.updateTime = updateTime;
    }

    public List<IngestionPipelineNodePayload> getNodes() {
        return nodes;
    }

    public void setNodes(List<IngestionPipelineNodePayload> nodes) {
        this.nodes = List.copyOf(Objects.requireNonNullElse(nodes, List.of()));
    }
}
